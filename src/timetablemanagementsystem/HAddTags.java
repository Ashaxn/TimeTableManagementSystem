    public ArrayList<HTagsModel> TagsList() {
        
        ArrayList<HTagsModel> TagsList = new ArrayList<>();
        try {
            String query = "SELECT * FROM TAGS";
            statement = connection.createStatement();
            ResultSet rs = statement.executeQuery(query);
            
            HTagsModel htagsmodel ;
            
            while(rs.next()){
                htagsmodel = new HTagsModel (rs.getInt("ID"), rs.getString("tag_name"), rs.getString("tag_code"), rs.getString("related_tag"));
                TagsList.add(htagsmodel);
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex + "Exception occured in TagList method");
            Logger.getLogger(HAddTags.class.getName()).log(Level.SEVERE, null, ex);         
        }
        return TagsList;
    }
    
   public void show_TagDetails(){
        ArrayList<HTagsModel> TagsList = TagsList();
        DefaultTableModel tableModel = (DefaultTableModel) display_tagstable.getModel();
        
        Object[] row = new Object[4];
        for (int i = 0; i < TagsList.size(); i++) {
            row[0] = TagsList.get(i).getID();
            row[1] = TagsList.get(i).gettagName();
            row[2] = TagsList.get(i).gettagCode();
            row[3] = TagsList.get(i).getrelatedTag();
            
            tableModel.addRow(row);
        }
    }
    
