    private void btn_taddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_taddActionPerformed
        // TODO add your handling code here:
        this.tag_name = tagName.getText();
        this.tag_code = tagCode.getText();
        this.related_tag = relatedTag.getSelectedItem().toString();
        
        if(tagName.getText().equals("") ||  tagCode.getText().equals("") || relatedTag.getSelectedItem().equals("") ) {
                    JOptionPane.showMessageDialog(null, "Please Fill All Fields.");
                
        } else {
            if ("" != tagName.getText()) {

                try {
                    String query_tagsexits = "select * from tags where tag_name='" + tagName.getText() + "'";
                    Statement st_tag = connection.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);

                    ResultSet rs1 = st_tag.executeQuery(query_tagsexits);
                    if (rs1.first()) {
                        JOptionPane.showMessageDialog(null, "This Tag Already Exist");

                    } else {
                        try {
                            String query = "insert into tags(tag_name, tag_code, related_tag) "
                                    + "values ( ?, ?, ? )";

                            preparedStmt = connection.prepareStatement(query);
                            preparedStmt.setString(1, tag_name);
                            preparedStmt.setString(2, tag_code);
                            preparedStmt.setString(3, related_tag);

                            preparedStmt.execute();

                            JOptionPane.showMessageDialog(null, "Tag Added Successfully. \n Thank You!");
                            DefaultTableModel model = (DefaultTableModel) display_tagstable.getModel();
                            model.setRowCount(0);
                            show_TagDetails();


                        } catch (SQLException e) {
                            JOptionPane.showMessageDialog(null, "Something went wrong! Please try again.");
                            System.err.println("Got an exception!");
                            System.err.println(e.getMessage());
                            Logger.getLogger(AAddBuildings.class.getName()).log(Level.SEVERE, null, e);
                        }

                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Something went wrong! Please try again.");
                    System.err.println("Got an exception!");
                    System.err.println(e.getMessage());
                    Logger.getLogger(AAddBuildings.class.getName()).log(Level.SEVERE, null, e);
                }

            }

        }
    }//GEN-LAST:event_btn_taddActionPerformed

