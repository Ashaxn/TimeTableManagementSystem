    private void btn_updateSGroupsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateSGroupsActionPerformed
        // TODO add your handling code here:
     
        String updated_AYS = AYS2.getText();
        String updated_program = PROGRAM2.getSelectedItem().toString();
        String updated_groupNo = GROUPNO2.getValue().toString();
        String updated_subgroupNo = SUBGROUPNO2.getValue().toString();
        String updated_groupID = GROUPID2.getText();
        String updated_subgroupID = SUBGROUPID2.getText();
            
        if (AYS2.getText().equals("") || PROGRAM2.getSelectedItem().equals("") || GROUPNO2.getValue().equals("") || SUBGROUPNO2.getValue().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select A Row To Update.");
        
        } else{  
            
           
             try {      
                 String query = "update SGROUPS " + "set AYS = ?, program = ?, groupNo = ?, subgroupNo = ?, groupID = ?, subGroupID = ?"
                                                    + "where ID= ?";

                preparedStmt = connection.prepareStatement(query);
                preparedStmt.setString(1, updated_AYS);
                preparedStmt.setString(2, updated_program );
                preparedStmt.setString(3, updated_groupNo);
                preparedStmt.setString(4, updated_subgroupNo);
                preparedStmt.setString(5, updated_groupID);
                preparedStmt.setString(6, updated_subgroupID );
                preparedStmt.setString(7, HSGroups_rowSelected);
               
                
                preparedStmt.execute();
                
                DefaultTableModel model = (DefaultTableModel) display_sgroupstable.getModel();
                model.setRowCount(0);
                show_SGroupDetails();
                JOptionPane.showMessageDialog(null, "Student Group details updated successfully");

                
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Something went wrong! Please try again.");
                System.err.println("Exception in Update operation : " + ex);
                System.err.println(ex.getMessage());
                Logger.getLogger(AAddBuildings.class.getName()).log(Level.SEVERE, null, ex);
            }                
        }

    }//GEN-LAST:event_btn_updateSGroupsActionPerformed

    private void btn_deleteSGroupsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteSGroupsActionPerformed
        // TODO add your handling code here:
         int opt = JOptionPane.showConfirmDialog(null, "Are you sure you want to Delete " + HSGroups_rowSelected + " - "
                + subGroupID + " details ?", "Delete", JOptionPane.YES_NO_OPTION);
        
        if (opt == 0) {
            try {                
                String query = "DELETE FROM SGROUPS WHERE ID =?";
                preparedStmt = connection.prepareStatement(query);
                preparedStmt.setString(1, HSGroups_rowSelected);                
                
                preparedStmt.execute();
                
                JOptionPane.showMessageDialog(null, "Student Group removed successfully from the Database");
                DefaultTableModel model = (DefaultTableModel) display_sgroupstable.getModel();
                model.setRowCount(0);
                show_SGroupDetails();
                
            }catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Something went wrong! Please try again.");
                System.err.println("Exception in delete operation : " + ex);
                System.err.println(ex.getMessage());
                Logger.getLogger(AAddBuildings.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }

    }//GEN-LAST:event_btn_deleteSGroupsActionPerformed

    private void display_sgroupstableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_display_sgroupstableMouseClicked
        // TODO add your handling code here:
        int i = display_sgroupstable.getSelectedRow();
        TableModel model = display_sgroupstable.getModel();
        AYS2.setText(model.getValueAt(i, 1).toString());
        PROGRAM2.setSelectedItem(model.getValueAt(i, 2).toString());
        GROUPNO2.setValue(model.getValueAt(i, 3));
        SUBGROUPNO2.setValue(model.getValueAt(i, 4));
        GROUPID2.setText(model.getValueAt(i, 5).toString());
        SUBGROUPID2.setText(model.getValueAt(i, 6).toString());

        HSGroups_rowSelected = model.getValueAt(i, 0).toString();
        AYS = model.getValueAt(i,1).toString();
        program = model.getValueAt(i,2).toString();
        groupNo = Integer.parseInt(model.getValueAt(i,3).toString());
        subGroupNo = Integer.parseInt(model.getValueAt(i,4).toString());
        groupID = model.getValueAt(i,5).toString();
        subGroupID = model.getValueAt(i,6).toString();
        

    }//GEN-LAST:event_display_sgroupstableMouseClicked

