    public ArrayList<HStudentGroupsModel> StudentGroupsList() {
        
        ArrayList<HStudentGroupsModel> StudentGroupsList = new ArrayList<>();
        try {
            String query = "SELECT * FROM SGROUPS";
            statement = connection.createStatement();
            ResultSet rs = statement.executeQuery(query);
            
            HStudentGroupsModel hstudentgroupsmodel ;
            
            while(rs.next()){
                hstudentgroupsmodel = new HStudentGroupsModel ( rs.getInt("ID"),rs.getString("AYS"), rs.getString("program"), rs.getInt("groupNo"),rs.getInt("subGroupNo"), rs.getString("groupID"), rs.getString("subGroupID"));
                StudentGroupsList.add(hstudentgroupsmodel);
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex + "Exception occured in TagList method");
            Logger.getLogger(HAddTags.class.getName()).log(Level.SEVERE, null, ex);         
        }
        return StudentGroupsList;
    }
    
    
     public void show_SGroupDetails(){
        ArrayList<HStudentGroupsModel>  StudentGroupsList =  StudentGroupsList();
        DefaultTableModel tableModel = (DefaultTableModel) display_sgroupstable.getModel();
        
        Object[] row = new Object[7];
        for (int i = 0; i <  StudentGroupsList.size(); i++) {
            row[0] =  StudentGroupsList.get(i).getID();
            row[1] =  StudentGroupsList.get(i).getAYS();
            row[2] =  StudentGroupsList.get(i).getprogram();
            row[3] =  StudentGroupsList.get(i).getgroupNo();
            row[4] =  StudentGroupsList.get(i).getsubGroupNo();
            row[5] =  StudentGroupsList.get(i).getgroupID();
            row[6] =  StudentGroupsList.get(i).getsubGroupID();
            
            tableModel.addRow(row);
        }
        
    }
   
