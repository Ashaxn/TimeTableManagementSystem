    private void btn_addsgroupsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addsgroupsActionPerformed
        // TODO add your handling code here:
        this.AYS = AcademicYear.getText();
        this.program = btn_program.getSelectedItem().toString();
        this.groupNo = (int) spin_groupNo.getValue();
        this.subGroupNo = (int) spin_subgroupNo.getValue();
        this.groupID = field_groupID.getText();
        this.subGroupID = field_subgroupID.getText();
        
        if(AcademicYear.getText().equals("") ||  btn_program.getSelectedItem().equals("") || spin_groupNo.getValue().equals("0") || spin_subgroupNo.getValue().equals("0")) {
                    JOptionPane.showMessageDialog(null, "Please Fill All Fields.");
                
        } else {
            if ("" != field_subgroupID.getText()) {

                try {
                    String query_sgroupsexits = "select * from Sgroups where subgroupID='" + field_subgroupID.getText() + "' ";
                    Statement st_tag = connection.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);

                    ResultSet rs1 = st_tag.executeQuery(query_sgroupsexits);
                    if (rs1.first()) {
                        JOptionPane.showMessageDialog(null, "This student Group Already Exist");

                    } else {
                        try {
                            String query = "insert into sgroups(AYS, program, groupNo, subgroupNo, groupID, subGroupID) "
                                    + "values ( ?, ?, ? ,?, ?, ?)";

                            preparedStmt = connection.prepareStatement(query);
                            preparedStmt.setString(1, AYS);
                            preparedStmt.setString(2, program);
                            preparedStmt.setInt(3, groupNo);
                            preparedStmt.setInt(4, subGroupNo);
                            preparedStmt.setString(5, groupID);
                            preparedStmt.setString(6, subGroupID);

                            preparedStmt.execute();

                            JOptionPane.showMessageDialog(null, "Tag Added Successfully. \n Thank You!");
                            DefaultTableModel model = (DefaultTableModel) display_sgroupstable.getModel();
                            model.setRowCount(0);
                            show_SGroupDetails();


                        } catch (SQLException e) {
                            JOptionPane.showMessageDialog(null, "Something went wrong! Please try again.");
                            System.err.println("Got an exception!");
                            System.err.println(e.getMessage());
                            Logger.getLogger(AAddBuildings.class.getName()).log(Level.SEVERE, null, e);
                        }

                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, "Something went wrong! Please try again.");
                    System.err.println("Got an exception!");
                    System.err.println(e.getMessage());
                    Logger.getLogger(AAddBuildings.class.getName()).log(Level.SEVERE, null, e);
                }

            }

        }
        
    }//GEN-LAST:event_btn_addsgroupsActionPerformed

